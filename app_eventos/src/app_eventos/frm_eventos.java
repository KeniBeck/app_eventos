    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
       if(txt_nombre.getText().equals("")||txt_fecha.getText().equals("")||txt_hora.getText().equals("")
               ||txt_ubicacion.getText().equals("")){
        JOptionPane.showMessageDialog(null, "faltan datos ","ERROR",JOptionPane.ERROR_MESSAGE);       
       }else{
       eventos [int_posiciones] = new cls_eventos(txt_nombre.getText(), txt_fecha.getText(), txt_hora.getText(), txt_ubicacion.getText());
       int_posiciones++;
          JOptionPane.showMessageDialog(null, "Evento registrado con exito ","Registro",JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_btn_registrarActionPerformed

    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        if (int_posiciones == 0) {
        JOptionPane.showMessageDialog(null, "No hay eventos registrados.", "Consulta", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // Limpia el JTextArea antes de agregar nuevos eventos
        txtarea_eventos.setText("");
        
        for (int i = 0; i < int_posiciones; i++) {
            cls_eventos evento = eventos[i];
            txtarea_eventos.append("Nombre: " + evento.getStr_nombre()+ "\n");
            txtarea_eventos.append("Fecha: " + evento.getStr_fecha()+ "\n");
            txtarea_eventos.append("Hora: " + evento.getStr_hora()+ "\n");
            txtarea_eventos.append("UbicaciÃ³n: " + evento.getStr_ubicacion()+ "\n");
            txtarea_eventos.append("------------------------------\n");
        }
    }
    }//GEN-LAST:event_btn_consultarActionPerformed
